package work3;

/**
 * Flyweight інтерфейс.
 *
 * <p>Оголошує операцію, яка приймає екстринзичний стан (координати).
 * Реалізації зберігають лише інтринзичний стан (спільний для багатьох об'єктів).</p>
 *
 * @author Денис
 * @since 1.0
 */
public interface IFlyweight {
    /**
     * Виконати операцію з переданим екстринзичним станом.
     *
     * <p>Метод повинен бути дешевим в плані виклику. Реальна логіка рендеру або обробки
     * інтегрується в конкретну реалізацію.</p>
     *
     * @param x координата X екстринзичного стану (приклад: позиція в рядку)
     * @param y координата Y екстринзичного стану
     */
    void operation(int x, int y);
}
